<b>Hinweise zur Benutzung:</b> 

Jeder Mitarbeiter dokumentiert seine Erfahrungen in  sog. "persönlichen Skills". Diese können sich entweder auf eine konkrete Projekterfahrung, eine Schulung oder ein Zertifikat beziehen. 

<a href="./manual.pdf">Kurzes Handbuch</a>

Jeder dieser "persönlichen Skills" bezieht sich immer auf einen "Skill" - eine Kenntnis / ein Thema wie z.B. Projekt-Management, die Beherrschung eines Tools oder auch IT-Architektur. Wenn man für sich einen persönlichen Skill einrichten will (z.B. Projekt-Management), dann prüft man zunächst, ob dieser Skill bereits existiert.
Existiert dieser noch nicht, legt man einen Skill an (der dann für alle gilt), und wählt sich als Mitarbeiter aus und legt dann einen persönlichen Skill an (entweder ein Projekt, in dem man gearbeitet hat, eine Schulung, die man besucht hat oder ein Zertifikat, was man erhalten hat.

Dann wählt man in der folgenden Maske den Skill aus (z.B. Projekt-Management), wählt den Skill-Level aus, den man meint erreicht zu haben und trägt dann in dem Feld seine Rolle und seine Aufgaben in dem Projekt ein. Das ist wichtig, denn es gibt ja durchaus sehr unterschiedliche Aufgaben, die man in einem Projekt durchführen kann.

Bei Schulungen und Zertifikaten kann man Erfahrungen und Schwerpunkte der Schulung angeben.
Dann wählt man rechts das Projekt, die Schulung oder das Zertifikat aus und ist fertig.

Zusätzlich kann man auch festhalten, ob man für ein Thema Interesse hat. Man kann z.B. ein Thema nicht gut beherrschen, aber trotzdem großes Interesse daran haben.man